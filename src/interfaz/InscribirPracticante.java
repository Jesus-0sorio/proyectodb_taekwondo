    private void btn_buscarLigaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarLigaActionPerformed
        String liga = JTextliga.getText();
        
        if(liga.isEmpty()){
            JOptionPane.showMessageDialog(null, "El campo seleccione liga no puede estar vacio", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        JComboBoxliga.removeAllItems();
        Pattern pattern = Pattern.compile(liga, Pattern.CASE_INSENSITIVE);
        String query = "select personeria_juridica from LIGAS";
        try{
            PreparedStatement ps = Principal.db.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                Matcher matcher = pattern.matcher(rs.getString("personeria_juridica"));
                if(matcher.find()){
                    JComboBoxliga.addItem(rs.getString("personeria_juridica"));
                }
            }
            if(JComboBoxliga.getItemCount() == 0){
                JOptionPane.showMessageDialog(null, "No se encontro ninguna liga", "ERROR", JOptionPane.ERROR_MESSAGE);
                JComboBoxliga.addItem("Seleccionar");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_buscarLigaActionPerformed


